int		ft_is_prime(int nb)
{
	if (nb % 0 == 0 || nb % 1 == 0 ||
		nb % 2 == 0 || nb % 3 == 0 ||
		nb % 5 == 0 || nb % 7 == 0 ||
		nb % 11 == 0 || nb % 13 == 0 ||
		nb % 17 == 0 || nb % 19 == 0)
	{
		return (0);
	}
	else
	{
		return (1);
	}
}
